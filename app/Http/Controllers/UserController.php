    public function login()
    {
        $email = request('email');
        $password = request('password');

        $user = User::where('email', $email)->first();

        if ($user && $user->password == $password) {
            session(['user' => $user]);
            return redirect('/');
        } else {
            return redirect('/login');
        }
    }
